const oracledb = require('oracledb');
require('dotenv').config();

// Load Oracle credentials from environment variables.
// Use .env to set these values before running this script.
const SYS_USER = process.env.SYS_USER || 'sys';
const SYS_PASSWORD = process.env.SYS_PASSWORD;
const SYS_CONNECTIONSTRING = process.env.SYS_CONNECTIONSTRING || 'localhost:1521/XE';

const APP_USER = process.env.ORACLE_USER;
const APP_PASSWORD = process.env.ORACLE_PASSWORD;
const APP_CONNECTIONSTRING = process.env.ORACLE_CONNECTIONSTRING;

if (!SYS_PASSWORD) {
  console.error('ERROR: Missing SYS_PASSWORD. Set SYS_USER and SYS_PASSWORD in .env or environment.');
  process.exit(1);
}

if (!APP_USER || !APP_PASSWORD || !APP_CONNECTIONSTRING) {
  console.error('ERROR: Missing ORACLE_USER, ORACLE_PASSWORD, or ORACLE_CONNECTIONSTRING. Add them to .env or environment.');
  process.exit(1);
}

const SYS_CONNECTION = {
  user: SYS_USER,
  password: SYS_PASSWORD,
  connectString: SYS_CONNECTIONSTRING,
  privilege: oracledb.SYSDBA
};

const APP_CONNECTION = {
  user: APP_USER,
  password: APP_PASSWORD,
  connectString: APP_CONNECTIONSTRING
};

async function setupDatabase() {
  let connection;
  
  try {
    console.log('Connecting to Oracle as SYS...');
    connection = await oracledb.getConnection(SYS_CONNECTION);
    console.log('Connected successfully!');

    // Create application user
    console.log(`\nCreating user ${APP_USER}...`);
    try {
      await connection.execute(`
        CREATE USER ${APP_USER} IDENTIFIED BY ${APP_PASSWORD}
      `);
      console.log(`User ${APP_USER} created`);
    } catch (err) {
      if (err.message && err.message.includes('ORA-01920')) {
        console.log(`User ${APP_USER} already exists`);
      } else {
        throw err;
      }
    }

    // Grant privileges
    console.log('Granting privileges...');
    try {
      await connection.execute(`GRANT CONNECT, RESOURCE, CREATE TABLE TO ${APP_USER}`);
      console.log('Privileges granted');
    } catch (err) {
      console.log('Privileges may already be granted:', err.message);
    }

    // Grant quota on the USERS tablespace so the app user can create objects
    console.log(`Granting quota on USERS tablespace to ${APP_USER}...`);
    try {
      await connection.execute(`ALTER USER ${APP_USER} QUOTA UNLIMITED ON USERS`);
      console.log(`Quota granted to ${APP_USER}`);
    } catch (err) {
      console.log('Tablespace quota may already be granted or unavailable:', err.message);
    }

    // Close SYS connection and connect as mgps_app to create tables
    await connection.close();
    
    console.log('\nConnecting as mgps_app...');
    connection = await oracledb.getConnection(APP_CONNECTION);
    console.log('Connected as mgps_app');

    // Create MGPS_USERS table
    console.log('\nCreating MGPS_USERS table...');
    try {
      await connection.execute(`
        CREATE TABLE MGPS_USERS (
          EMP_ID        VARCHAR2(50) PRIMARY KEY,
          NAME          VARCHAR2(200) NOT NULL,
          DESIGNATION   VARCHAR2(100),
          PASSWORD_HASH VARCHAR2(512) NOT NULL,
          SALT          VARCHAR2(64) NOT NULL,
          DEPT_CODE     VARCHAR2(50),
          CREATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP
        )
      `);
      console.log('MGPS_USERS table created');
    } catch (err) {
      if (err.message && err.message.includes('ORA-00955')) {
        console.log('MGPS_USERS table already exists');
      } else {
        throw err;
      }
    }

    // Create MGPS_ROLES table
    console.log('Creating MGPS_ROLES table...');
    try {
      await connection.execute(`
        CREATE TABLE MGPS_ROLES (
          ROLE_ID   NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
          ROLE_NAME VARCHAR2(50) UNIQUE NOT NULL
        )
      `);
      console.log('MGPS_ROLES table created');
    } catch (err) {
      if (err.message && err.message.includes('ORA-00955')) {
        console.log('MGPS_ROLES table already exists');
      } else {
        throw err;
      }
    }

    // Create MGPS_USER_ROLES table
    console.log('Creating MGPS_USER_ROLES table...');
    try {
      await connection.execute(`
        CREATE TABLE MGPS_USER_ROLES (
          EMP_ID  VARCHAR2(50) REFERENCES MGPS_USERS(EMP_ID) ON DELETE CASCADE,
          ROLE_ID NUMBER REFERENCES MGPS_ROLES(ROLE_ID),
          CONSTRAINT PK_USER_ROLES PRIMARY KEY (EMP_ID, ROLE_ID)
        )
      `);
      console.log('MGPS_USER_ROLES table created');
    } catch (err) {
      if (err.message && err.message.includes('ORA-00955')) {
        console.log('MGPS_USER_ROLES table already exists');
      } else {
        throw err;
      }
    }

    // Create MGPS_LOCATIONS table
    console.log('Creating MGPS_LOCATIONS table...');
    try {
      await connection.execute(`
        CREATE TABLE MGPS_LOCATIONS (
          LOCATION_ID   NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
          EMP_ID        VARCHAR2(50) REFERENCES MGPS_USERS(EMP_ID) ON DELETE CASCADE,
          LATITUDE      NUMBER(10, 8) NOT NULL,
          LONGITUDE     NUMBER(11, 8) NOT NULL,
          TRACKED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP
        )
      `);
      console.log('MGPS_LOCATIONS table created');
    } catch (err) {
      if (err.message && err.message.includes('ORA-00955')) {
        console.log('MGPS_LOCATIONS table already exists');
      } else {
        throw err;
      }
    }

    // Create MGPS_GATEPASSES table
    console.log('Creating MGPS_GATEPASSES table...');
    try {
      await connection.execute(`
        CREATE TABLE MGPS_GATEPASSES (
          GATEPASS_ID   NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
          EMP_ID        VARCHAR2(50) REFERENCES MGPS_USERS(EMP_ID) ON DELETE CASCADE,
          GATEPASS_TYPE VARCHAR2(50) NOT NULL,
          REASON        VARCHAR2(500) NOT NULL,
          OUT_TIME      TIMESTAMP,
          IN_TIME       TIMESTAMP,
          STATUS        VARCHAR2(50) DEFAULT 'PENDING',
          APPROVED_BY   VARCHAR2(50) REFERENCES MGPS_USERS(EMP_ID),
          CREATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP
        )
      `);
      console.log('MGPS_GATEPASSES table created');
    } catch (err) {
      if (err.message && err.message.includes('ORA-00955')) {
        console.log('MGPS_GATEPASSES table already exists');
      } else {
        throw err;
      }
    }

    // Create MGPS_MATERIAL_PASSES table
    console.log('Creating MGPS_MATERIAL_PASSES table...');
    try {
      await connection.execute(`
        CREATE TABLE MGPS_MATERIAL_PASSES (
          PASS_ID       NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
          EMP_ID        VARCHAR2(50) REFERENCES MGPS_USERS(EMP_ID) ON DELETE CASCADE,
          MATERIAL_DESC VARCHAR2(500) NOT NULL,
          QUANTITY      NUMBER NOT NULL,
          PASS_TYPE     VARCHAR2(50) NOT NULL,
          OUT_TIME      TIMESTAMP,
          RETURN_TIME   TIMESTAMP,
          STATUS        VARCHAR2(50) DEFAULT 'PENDING',
          APPROVED_BY   VARCHAR2(50) REFERENCES MGPS_USERS(EMP_ID),
          CREATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP
        )
      `);
      console.log('MGPS_MATERIAL_PASSES table created');
    } catch (err) {
      if (err.message && err.message.includes('ORA-00955')) {
        console.log('MGPS_MATERIAL_PASSES table already exists');
      } else {
        throw err;
      }
    }

    // Insert default roles
    console.log('\nInserting default roles...');
    const roles = ['EMP', 'E1', 'EL', 'CISF'];
    for (const role of roles) {
      try {
        await connection.execute(`INSERT INTO MGPS_ROLES (ROLE_NAME) VALUES ('${role}')`);
        console.log(`Role '${role}' inserted`);
      } catch (err) {
        if (err.message && err.message.includes('ORA-00001')) {
          console.log(`Role '${role}' already exists`);
        } else {
          console.log(`Role '${role}' error (may already exist):`, err.message);
        }
      }
    }

    await connection.commit();
    console.log('\nDatabase setup completed successfully!');

  } catch (err) {
    console.error('Error during database setup:', err);
    process.exit(1);
  } finally {
    if (connection) {
      try {
        await connection.close();
      } catch (err) {
        console.error('Error closing connection:', err);
      }
    }
  }
}

setupDatabase();
