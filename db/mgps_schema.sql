-- MGPS schema for Oracle XE
-- Run as a DBA to create user, then run the table DDL as that user.

-- 1) Create application user (run as SYS or DBA):
-- CREATE USER mgps_app IDENTIFIED BY mgps_password;
-- GRANT CONNECT, RESOURCE TO mgps_app;

-- 2) Connect as mgps_app and run DDL below:

CREATE TABLE MGPS_USERS (
  EMP_ID        VARCHAR2(50) PRIMARY KEY,
  NAME          VARCHAR2(200) NOT NULL,
  DESIGNATION   VARCHAR2(100),
  PASSWORD_HASH VARCHAR2(512) NOT NULL,
  SALT          VARCHAR2(64) NOT NULL,
  DEPT_CODE     VARCHAR2(50),
  CREATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE TABLE MGPS_ROLES (
  ROLE_ID   NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  ROLE_NAME VARCHAR2(50) UNIQUE NOT NULL
);

CREATE TABLE MGPS_USER_ROLES (
  EMP_ID  VARCHAR2(50) REFERENCES MGPS_USERS(EMP_ID) ON DELETE CASCADE,
  ROLE_ID NUMBER REFERENCES MGPS_ROLES(ROLE_ID),
  CONSTRAINT PK_USER_ROLES PRIMARY KEY (EMP_ID, ROLE_ID)
);

CREATE TABLE MGPS_LOCATIONS (
  LOCATION_ID   NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  EMP_ID        VARCHAR2(50) REFERENCES MGPS_USERS(EMP_ID) ON DELETE CASCADE,
  LATITUDE      NUMBER(10, 8) NOT NULL,
  LONGITUDE     NUMBER(11, 8) NOT NULL,
  TRACKED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE TABLE MGPS_GATEPASSES (
  GATEPASS_ID   NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  EMP_ID        VARCHAR2(50) REFERENCES MGPS_USERS(EMP_ID) ON DELETE CASCADE,
  GATEPASS_TYPE VARCHAR2(50) NOT NULL,
  REASON        VARCHAR2(500) NOT NULL,
  OUT_TIME      TIMESTAMP,
  IN_TIME       TIMESTAMP,
  STATUS        VARCHAR2(50) DEFAULT 'PENDING',
  APPROVED_BY   VARCHAR2(50) REFERENCES MGPS_USERS(EMP_ID),
  CREATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE TABLE MGPS_MATERIAL_PASSES (
  PASS_ID       NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  EMP_ID        VARCHAR2(50) REFERENCES MGPS_USERS(EMP_ID) ON DELETE CASCADE,
  MATERIAL_DESC VARCHAR2(500) NOT NULL,
  QUANTITY      NUMBER NOT NULL,
  PASS_TYPE     VARCHAR2(50) NOT NULL,
  OUT_TIME      TIMESTAMP,
  RETURN_TIME   TIMESTAMP,
  STATUS        VARCHAR2(50) DEFAULT 'PENDING',
  APPROVED_BY   VARCHAR2(50) REFERENCES MGPS_USERS(EMP_ID),
  CREATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP
);

